"""
Repetições
while (enquanto)
Executa uma ação enquanto uma condição for verdadeira
Loop infinito -> Quando um código não tem fim
"""
contador = 0

while contador < 10:        # Enquanto meu contador for menor que 10
    # print(contador)         Se colocar o print antes, ele vai iniciar no 0 e terminar no 9
    contador = contador + 1 
    print(contador)         # Se colocar o print depois, ele vai iniciar no 1 e terminar no 10

print('Acabou')