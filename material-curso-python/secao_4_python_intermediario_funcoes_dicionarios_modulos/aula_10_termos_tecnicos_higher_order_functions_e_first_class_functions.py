"""
Termos técnicos: Higher Order Functions e First-Class Functions

Academicamente, esses termos têm significados diferentes, apesar de muitas vezes aparecerem juntos no código
"""

"""
First-Class Functions (Funções de primeira classe)

São funções tratadas como qualquer outro tipo de dados:
- Podem ser atribuídas a variáveis
- Podem ser passadas como argumentos
- Podem ser retornadas por outras funções
"""

"""
Higher Order Functions (Funções de ordem superior)

São funções que:
- Recebem outras funções como argumento e/ou retornam outras funções
"""

"""
Observação importante:
- Uma função só pode ser Higher Order porque funções são First-Class
- Na prática, os termos costumam aparecer juntos
- O comportamento do código não muda, apenas o nome do conceito
"""